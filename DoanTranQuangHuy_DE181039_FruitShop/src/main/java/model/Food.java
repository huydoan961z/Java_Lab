package model;

/**
 *
 * @author aser
 */
public class Food
{
    private int id, price, quantity;
    private String name, origin;

    public Food() {
    }

    public Food(int id, int price, int quantity, String name, String origin) {
        this.id = id;
        this.price = price;
        this.quantity = quantity;
        this.name = name;
        this.origin = origin;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getOrigin() {
        return origin;
    }

    public void setOrigin(String origin) {
        this.origin = origin;
    }
}